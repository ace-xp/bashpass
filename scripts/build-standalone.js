import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const PLUGIN = join(ROOT, 'plugins', 'bashpass');

function read(rel) {
  return readFileSync(join(PLUGIN, rel), 'utf8');
}

const allowlist = JSON.parse(read('data/builtin-allowlist.json'));
const denylist = JSON.parse(read('data/builtin-denylist.json'));

// 简单 inline：把所有 lib 文件按依赖顺序拼接，去掉 import/export
function stripModule(src) {
  return src
    .replace(/^import .+ from .+;\s*$/gm, '')
    .replace(/^export\s+/gm, '');
}

const matcher = stripModule(read('lib/matcher.js'));
const parser = stripModule(read('lib/parser.js'));
const settings = stripModule(read('lib/settings-loader.js'));
const ack = stripModule(read('lib/ack.js'));
const decision = stripModule(read('lib/decision.js'));
const hook = stripModule(read('hooks/pre-tool-use.js'));

const bundle = `#!/usr/bin/env node
// bashpass standalone bundle - generated by scripts/build-standalone.js
// DO NOT EDIT BY HAND

import { readFileSync, existsSync, mkdirSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { homedir } from 'node:os';

const BUILTIN_ALLOWLIST = ${JSON.stringify(allowlist.patterns)};
const BUILTIN_DENYLIST = ${JSON.stringify(denylist.patterns)};

${matcher}

${parser}

${settings.replace(/import\s*{\s*bashRuleToGlob\s*}\s*from\s*['"]\.\/matcher\.js['"];?/g, '')}

${ack}

${decision}

// === hook entry (inlined) ===
${hook
  .replace(/^#!.*\n/, '')
  .replace(/import\s*{[^}]+}\s*from\s*['"][^'"]+['"];?/g, '')
  .replace(/^const __dirname\s*=.*\n/m, '')
  .replace(/^const __filename\s*=.*\n/m, '')
  .replace(/loadBuiltinList\('builtin-allowlist\.json'\)/g, 'BUILTIN_ALLOWLIST')
  .replace(/loadBuiltinList\('builtin-denylist\.json'\)/g, 'BUILTIN_DENYLIST')
  .replace(/function loadBuiltinList[\s\S]*?\n}\n/, '')
  .replace(/^main\(\);\s*$/m, '')}

main();
`;

mkdirSync(join(ROOT, 'standalone'), { recursive: true });
writeFileSync(join(ROOT, 'standalone', 'pre-tool-use.bundle.js'), bundle);
console.log('Built standalone/pre-tool-use.bundle.js');
